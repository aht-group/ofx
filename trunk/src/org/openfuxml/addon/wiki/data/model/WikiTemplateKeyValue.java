package org.openfuxml.addon.wiki.data.model;

public class WikiTemplateKeyValue
{
	private String key;
	private String keyName;
	private String valueMarkup;
	
	public String getKey() {return key;}
	public void setKey(String key) {this.key = key;}
	
	public String getKeyName() {return keyName;}
	public void setKeyName(String keyName) {this.keyName = keyName;}
	
	public String getValueMarkup() {return valueMarkup;}
	public void setValueMarkup(String valueMarkup) {this.valueMarkup = valueMarkup;}
}
